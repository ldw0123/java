package p220124;

public class Day03_08 {

	public static void main(String[] args) {
		int a = 10;
		int b = 5;
		
		System.out.println(a > b); 		// true
		System.out.println(a >= b); 	// true
		System.out.println(a < b); 		// false
		System.out.println(a <= b); 	// false
		System.out.println(a == b); 	// false
		System.out.println(a != b); 	// true
	}

}
